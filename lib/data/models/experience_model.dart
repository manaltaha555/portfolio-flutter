class ExperienceModel {
  final String companyName;
  final String role;
  final String info;
  final String location;
  final String image;
  final List<String> responsibilities;

  ExperienceModel({
    required this.companyName,
    required this.role,
    required this.info,
    required this.location,
    required this.image,
    required this.responsibilities,
  });
}